class Person {

  constructor(firstName, secondName, login) {
    this.firstName = firstName;
    this.secondName = secondName;
    this.login = login;
  }

  getFirstName() {
    return this.firstName;
  }

  getSecondName() {
    return  this.secondName
  }

  getLogin() {
    return this.login
  }
  
}
